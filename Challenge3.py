'''
Write a program to work out the areas of a triangle.
Collect the width and height of the rectangle from the keyboard.
Calculate the area. 
Display the result.

Extension:
Display the volume of the cuboid.
'''
width = int(input('Enter width: '))
length = int(input('Enter length: '))
height = int(input('Enter height: '))
print('Area: ',width*length)
print('Volume: ',width*length*height)